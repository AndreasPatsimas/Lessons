package paragraphs;

public class WhileLoop_3 {

    public static void main(String[] args) {

        int i = 0;
        while (i < 5) { // όσο
            System.out.println(i);
            i++;
        }

        System.out.println("----------------------------------");

        int j = 3;
        do {
            System.out.println(j);
            j++;
        }
        while (j < 7); // μέχρις ότου


    }
}
